#include "libft.h"

void	*ft_calloc(size_t number, size_t size)
{
	char			*dst;
	unsigned int	i;
	unsigned int	sum;

	sum = number * size;
	dst = malloc(sum);
	if (!dst)
		return (NULL);
	i = 0;
	while (sum--)
	{
		dst[i] = 0;
		i++;
	}
	return ((void *)dst);
}
