#include "libft.h"

char	*ft_strrchr(const char *str, int ch)
{
	char			*ptr;

	ptr = NULL;
	while (*str)
	{
		if (*str == (char)ch)
			ptr = (char *)str;
		str++;
	}
	if ((char)ch == *str)
		return ((char *)str);
	return (ptr);
}
