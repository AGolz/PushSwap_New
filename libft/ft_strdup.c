#include "libft.h"

char	*ft_strdup(const char *str)
{
	char	*dest;

	dest = (char *)malloc(sizeof(*dest) * (ft_strlen(str) + 1));
	if (dest)
		dest = ft_strcpy(dest, str);
	else
		dest = ((void *)0);
	return (dest);
}
